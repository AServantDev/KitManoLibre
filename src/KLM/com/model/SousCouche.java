package KLM.com.model;

public class SousCouche {

	private int idSousCouche;
	private String nomSC;
	private double prix;
	private double contenanceSC;
	private String couleurSC;
	private String lienSC;
	private String imgSC;
	
	public int getIdSousCouche() {
		return idSousCouche;
	}
	public void setIdSousCouche(int idSousCouche) {
		this.idSousCouche = idSousCouche;
	}
	public String getNomSC() {
		return nomSC;
	}
	public void setNomSC(String nomSC) {
		this.nomSC = nomSC;
	}
	public double getPrix() {
		return prix;
	}
	public void setPrix(double prix) {
		this.prix = prix;
	}
	public double getContenanceSC() {
		return contenanceSC;
	}
	public void setContenanceSC(double contenanceSC) {
		this.contenanceSC = contenanceSC;
	}
	public String getCouleurSC() {
		return couleurSC;
	}
	public void setCouleurSC(String couleurSC) {
		this.couleurSC = couleurSC;
	}
	public String getLienSC() {
		return lienSC;
	}
	public void setLienSC(String lienSC) {
		this.lienSC = lienSC;
	}
	public String getImgSC() {
		return imgSC;
	}
	public void setImgSC(String imgSC) {
		this.imgSC = imgSC;
	}
	
	
	
}
